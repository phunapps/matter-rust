//! Byte-parity / roundtrip-floor vectors for the OTA Provider command path.
//!
//! ## What this proves (F1)
//!
//! A `QueryImage` flows through the **full server-side IM path** — a client
//! `InvokeRequestMessage` ([`build_invoke_request`] + the generated
//! [`encode_query_image`](prov::encode_query_image)) is decoded by
//! [`parse_invoke_request`], the decoded command-fields drive
//! [`handle_query_image`], the result is framed by
//! [`build_invoke_response_command`], and that `InvokeResponseMessage` decodes
//! back via [`parse_invoke_response`] + the generated `QueryImageResponse::decode`
//! to the expected fields. This exercises Tasks 1–4 composed through the real IM
//! envelope, and freezes the **command-fields TLV** (the cluster-level wire) as
//! hex so an accidental tag/type/layout change is caught in review.
//!
//! ## The gap (deferred to F4, by design)
//!
//! These vectors are the **roundtrip floor** the plan names: they are
//! self-generated by our own client+server codecs, **not** captured from chip's
//! `ota-provider-app` ↔ `ota-requestor-app`. Capturing the decrypted,
//! CASE-encrypted `QueryImage`/`QueryImageResponse` bytes from the reference apps
//! needs both apps plus a decrypted-payload extraction path and is impractical at
//! F1 (no connectedhomeip checkout in this environment). Per the F design the
//! **live `ota-requestor-app` is the PRIMARY wire oracle** and arrives in F4: it
//! rejects a malformed `QueryImageResponse`/BDX, so a successful
//! announce→query→download→apply is itself strong wire evidence. Until then the
//! frozen hex below guards against wire-layout drift.
//!
//! The frozen command-fields hex was emitted by the codecs under test and
//! verified field-by-field below; it is **not** an independent oracle. The IM
//! envelope itself is already byte-parity-tested against matter.js in
//! `matter-interaction`, so only the OTA cluster-level fields are frozen here.

#![allow(clippy::unwrap_used, clippy::expect_used)]
