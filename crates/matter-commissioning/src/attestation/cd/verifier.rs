/// Verify an ECDSA-P256 / SHA-256 signature in fixed (raw r||s, 64
/// bytes) form against a SEC1-uncompressed P-256 public key.
///
/// Maps any verification failure to
/// [`AttestationError::CertificationDeclarationSignatureInvalid`] â€”
/// the caller (the per-root loop in
/// [`verify_certification_declaration`]) only cares whether *some*
/// trusted root accepted the signature, not which one rejected it.
fn verify_ecdsa_p256_sha256_fixed(
    public_key: &[u8],
    msg: &[u8],
    sig: &[u8],
) -> Result<(), AttestationError> {
    use ring::signature::{UnparsedPublicKey, ECDSA_P256_SHA256_FIXED};
    let pk = UnparsedPublicKey::new(&ECDSA_P256_SHA256_FIXED, public_key);
    pk.verify(msg, sig)
        .map_err(|_| AttestationError::CertificationDeclarationSignatureInvalid)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn with_csa_test_roots_constructs_at_t28_with_empty_store() {
        // M6.4.3 T29 replaces parse_pem_public_key with a real PEM
        // parser, at which point this assertion tightens to
        // `assert_eq!(trust.len(), 1)`. For T28's placeholder shape,
        // the constructor must not panic and must return a trust store
        // (possibly empty).
        let trust = CdSigningRoots::with_csa_test_roots();
        assert!(
            trust.len() <= 1,
            "T28 placeholder yields 0 roots; T29 yields exactly 1"
        );
    }

    #[test]
    #[allow(clippy::unwrap_used)] // Test-code carve-out: see CLAUDE.md.
    fn from_pem_empty_input_yields_empty_trust_store() {
        let trust = CdSigningRoots::from_pem(&[]).unwrap();
        assert!(trust.is_empty());
        assert_eq!(trust.len(), 0);
    }
}
